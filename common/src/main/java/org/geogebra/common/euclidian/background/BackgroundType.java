package org.geogebra.common.euclidian.background;

/**
 * Types of MOW backgrounds.
 * 
 * @author laszlso
 *
 */
public enum BackgroundType {
	/** no ruler as background */
	NONE,

	/** horizontal lines */
	RULER,

	/** small squares */
	SQUARE_SMALL,

	/** big squares */
	SQUARE_BIG,

	/** SVG as background */
	SVG
}
